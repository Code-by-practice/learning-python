for i in range(5):
  print(i)

print('\n')

for i in range(5, 10):
  print(i)

print('\n')

# (range start, range end, step) 
for i in range(0, 10, 3):
  print(i)

print('\n')

for i in range(-10, -100, -30):
  print(i)

print('\n')

